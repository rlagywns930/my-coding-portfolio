## 양수, 0, 음수 판단

# 사용자로부터 정수를 입력받아 num 변수에 저장
num = int(input("정수를 입력하세요: "))

# 입력 받은 숫자가 양수, 0, 음수인지 판단
if num > 0:
    print("양수입니다.")  #num이 0보다 크면 양수
elif num == 0:
    print("0 입니다.")  #num이 0과 같으면 0
else:
    print("음수입니다.")  #num이 0보다 작으면 음수
